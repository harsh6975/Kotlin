fun main() {

    // variable with data type
    val name: String = "Harsh Sinha"
    println(name)

    // variable without data type
    val name1 = "Harsh Sinha"
    println(name1)

    // val is immutable i.e cann't be reassinged
    // name1="Harsh"

    // var is mutable i.e can be reassinged
    var name2 = "Harsh Sinha"
    println(name2)

    name2 = "Sinha"
    println(name2)
}
